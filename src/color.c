static void getColor(uint32_t color, int* r, int* g, int* b) {
    *r = (color >> 8 * 0) & 0xFF;
    *g = (color >> 8 * 1) & 0xFF;
    *b = (color >> 8 * 2) & 0xFF;
    int a = (color >> 8 * 3) & 0xFF;
    *r *= a / 255.0f;
    *g *= a / 255.0f;
    *b *= a / 255.0f;
}

void setTrueColor(uint32_t color, int is_bg) {
    int r, g, b;
    getColor(color, &r, &g, &b);
    printf("\x1b[%d;2;%d;%d;%dm", is_bg ? 48 : 38, r, g, b);
}

