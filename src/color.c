void set256Color(uint32_t color, int is_bg) {
    int r, g, b;
    getColor(color, &r, &g, &b);
    int index = 0;
    int min_dist = INT32_MAX;
    for (int i = 0; i < 256; i++) {
        int dr = r - rgb256[i][0];
        int dg = g - rgb256[i][1];
        int db = b - rgb256[i][2];
        int dist = dr * dr + dg * dg + db * db;
        if (dist < min_dist) {
            min_dist = dist;
            index = i;
        }
    }
    printf("\x1b[%d;5;%dm", is_bg ? 48 : 38, index);
}
