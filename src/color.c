uint32_t getColorSqrDist(Color a, Color b) {
    int dr = a.r - b.r;
    int dg = a.g - b.g;
    int db = a.b - b.b;
    return dr * dr + dg * dg + db * db;
}

void setTrueColor(uint32_t color, int is_bg) {
    Color c = {.color = color};
    printf("\x1b[%d;2;%d;%d;%dm", is_bg ? 48 : 38, c.r, c.g, c.b);
}

